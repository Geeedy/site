"""Генератор SVG-инфографик для страниц услуг skill-dev.ai.
Типы: hbars (горизонтальные бары), steps (этапы), compare (колонки), stats (плитки), flow (поток), ladder (лестница).
Единая дизайн-система; все подписи русские; данные — из брифов проекта."""
import os, html

INK, MUT, ACC, GRN, AMB, RED = "#0b0b0b", "#52514e", "#2a78d6", "#008300", "#c9750a", "#c03535"
SURF, BORD, LIGHT = "#ffffff", "#e1e0d9", "#f4f4f1"
FONT = "system-ui,-apple-system,'Segoe UI',Arial,sans-serif"
W = 900

def esc(s): return html.escape(str(s))

def wrap(text, maxchars, maxlines=3):
    """Реальные переносы + word-wrap: строки не длиннее maxchars."""
    out = []
    for part in str(text).split("\n"):
        words, cur = part.split(), ""
        for w in words:
            if cur and len(cur) + 1 + len(w) > maxchars:
                out.append(cur); cur = w
            else:
                cur = (cur + " " + w).strip()
        if cur: out.append(cur)
    return out[:maxlines]



def head(h, title):
    return (f'<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 {W} {h}" font-family="{FONT}" '
            f'role="img" aria-label="{esc(title)}">'
            f'<rect width="{W}" height="{h}" fill="{SURF}" rx="14"/>'
            f'<rect x="1" y="1" width="{W-2}" height="{h-2}" fill="none" stroke="{BORD}" rx="13"/>'
            f'<text x="36" y="52" font-size="24" font-weight="700" fill="{INK}">{esc(title)}</text>')

def footer_note(h, note):
    if not note: return ''
    lines = wrap(note, 108, 2)
    return ''.join(f'<text x="36" y="{h-22-(len(lines)-1-k)*16}" font-size="13" fill="{MUT}">{esc(ln)}</text>'
                   for k, ln in enumerate(lines))

def hbars(title, rows, note="", vmax=None):
    # rows: [(label, value_text, fraction 0..1, color)]
    bar_h, gap, top = 34, 18, 84
    h = top + len(rows)*(bar_h+gap) + 56
    s = head(h, title)
    label_w, bar_x, bar_w = 300, 320, W-320-150
    for i,(lab, vtext, frac, col) in enumerate(rows):
        y = top + i*(bar_h+gap)
        lab_lines = wrap(lab, 36, 2)
        ly0 = y + bar_h/2 + 5 - (9 if len(lab_lines) > 1 else 0)
        for k, ln_t in enumerate(lab_lines):
            s += f'<text x="{label_w}" y="{ly0+k*18}" font-size="15" fill="{INK}" text-anchor="end">{esc(ln_t)}</text>'
        s += f'<rect x="{bar_x}" y="{y}" width="{bar_w}" height="{bar_h}" fill="{LIGHT}" rx="6"/>'
        w = max(int(bar_w*min(frac,1.0)), 8)
        s += f'<rect x="{bar_x}" y="{y}" width="{w}" height="{bar_h}" fill="{col}" rx="6"/>'
        s += f'<text x="{bar_x+w+12}" y="{y+bar_h/2+5}" font-size="15" font-weight="700" fill="{INK}">{esc(vtext)}</text>'
    s += footer_note(h, note) + '</svg>'
    return s

def steps(title, items, note=""):
    # items: [(num_label, text, subtext)]
    row_h, top = 74, 84
    h = top + len(items)*row_h + 50
    s = head(h, title)
    cx = 66
    for i,(nlab, text, sub) in enumerate(items):
        y = top + i*row_h
        if i < len(items)-1:
            s += f'<line x1="{cx}" y1="{y+27}" x2="{cx}" y2="{y+row_h+2}" stroke="{BORD}" stroke-width="3"/>'
        s += f'<circle cx="{cx}" cy="{y+14}" r="22" fill="{ACC}"/>'
        s += f'<text x="{cx}" y="{y+20}" font-size="16" font-weight="700" fill="#fff" text-anchor="middle">{esc(nlab)}</text>'
        s += f'<text x="{cx+44}" y="{y+12}" font-size="17" font-weight="700" fill="{INK}">{esc(text)}</text>'
        if sub:
            for k, ln_t in enumerate(wrap(sub, 88, 2)):
                s += f'<text x="{cx+44}" y="{y+36+k*18}" font-size="14" fill="{MUT}">{esc(ln_t)}</text>'
    s += footer_note(h, note) + '</svg>'
    return s

def compare(title, cols, rows, note=""):
    # cols: [(header, color)]; rows: [[cell,...] first cell = row label]
    n = len(cols)
    col_w = (W-72-210)//n
    row_h, top = 56, 96
    h = top + (len(rows))*row_h + 70
    s = head(h, title)
    for j,(hd, col) in enumerate(cols):
        x = 246 + j*col_w
        s += f'<rect x="{x}" y="{top-34}" width="{col_w-14}" height="40" fill="{col}" rx="8"/>'
        hd1 = wrap(hd, max(10, int((col_w - 20) / 8.8)), 1)[0]
        s += f'<text x="{x+(col_w-14)/2}" y="{top-8}" font-size="16" font-weight="700" fill="#fff" text-anchor="middle">{esc(hd1)}</text>'
    for i,row in enumerate(rows):
        y = top + i*row_h + 18
        if i % 2 == 0:
            s += f'<rect x="30" y="{y-24}" width="{W-60}" height="{row_h-4}" fill="{LIGHT}" rx="8"/>'
        s += f'<text x="42" y="{y+6}" font-size="14.5" font-weight="700" fill="{INK}">{esc(row[0])}</text>'
        for j,cell in enumerate(row[1:]):
            x = 246 + j*col_w
            maxc = max(10, int((col_w - 24) / 7.7))
            lines = wrap(cell, maxc, 2)
            for k,ln in enumerate(lines):
                s += f'<text x="{x+8}" y="{y+6+k*18-(9 if len(lines)>1 else 0)}" font-size="14" fill="{MUT}">{esc(ln)}</text>'
    s += footer_note(h, note) + '</svg>'
    return s

def stats(title, tiles, note=""):
    # tiles: [(big, caption, color)]
    n = len(tiles)
    tile_w = (W-72-(n-1)*18)//n
    top, tile_h = 88, 150
    h = top + tile_h + 62
    s = head(h, title)
    for i,(big, cap, col) in enumerate(tiles):
        x = 36 + i*(tile_w+18)
        s += f'<rect x="{x}" y="{top}" width="{tile_w}" height="{tile_h}" fill="{LIGHT}" rx="12"/>'
        s += f'<rect x="{x}" y="{top}" width="{tile_w}" height="6" fill="{col}" rx="3"/>'
        bfs = 30 if len(str(big)) <= 10 else (24 if len(str(big)) <= 14 else 20)
        s += f'<text x="{x+18}" y="{top+64}" font-size="{bfs}" font-weight="800" fill="{INK}">{esc(big)}</text>'
        maxc = max(12, int((tile_w - 36) / 7.4))
        for k,ln in enumerate(wrap(cap, maxc, 3)):
            s += f'<text x="{x+18}" y="{top+94+k*19}" font-size="13.5" fill="{MUT}">{esc(ln)}</text>'
    s += footer_note(h, note) + '</svg>'
    return s

def flow(title, nodes, note=""):
    # nodes: [(text, sub, color)] — горизонтальный поток со стрелками
    n = len(nodes)
    node_w = (W-72-(n-1)*46)//n
    top, node_h = 96, 118
    h = top + node_h + 66
    s = head(h, title)
    for i,(text, sub, col) in enumerate(nodes):
        x = 36 + i*(node_w+46)
        s += f'<rect x="{x}" y="{top}" width="{node_w}" height="{node_h}" fill="{LIGHT}" rx="12"/>'
        s += f'<rect x="{x}" y="{top}" width="{node_w}" height="6" fill="{col}" rx="3"/>'
        for k,ln in enumerate(wrap(text, max(10, int((node_w - 14) / 8.6)), 2)):
            s += f'<text x="{x+node_w/2}" y="{top+46+k*20}" font-size="15.5" font-weight="700" fill="{INK}" text-anchor="middle">{esc(ln)}</text>'
        if sub:
            for k,ln in enumerate(wrap(sub, max(12, int((node_w - 12) / 7.0)), 2)):
                s += f'<text x="{x+node_w/2}" y="{top+88+k*17}" font-size="12.5" fill="{MUT}" text-anchor="middle">{esc(ln)}</text>'
        if i < n-1:
            ax = x+node_w+8
            s += f'<path d="M {ax} {top+node_h/2} l 26 0 m -8 -8 l 8 8 l -8 8" stroke="{ACC}" stroke-width="3.5" fill="none" stroke-linecap="round" stroke-linejoin="round"/>'
    s += footer_note(h, note) + '</svg>'
    return s

def ladder(title, items, note=""):
    # items: [(label, price, term)] — восходящая лестница
    n = len(items)
    st_w = (W-90)//n
    base_y, min_h, step = 388, 74, 44
    h = base_y + 74
    s = head(h, title)
    for i,(lab, price, term) in enumerate(items):
        x = 45 + i*st_w
        sh = min_h + i*step
        y = base_y - sh
        col = ACC if i < n-1 else GRN
        s += f'<rect x="{x}" y="{y}" width="{st_w-10}" height="{sh}" fill="{col}" opacity="{0.55+0.075*i}" rx="8"/>'
        for k,ln in enumerate(wrap(lab, max(9, int((st_w - 18) / 7.6)), 2)):
            s += f'<text x="{x+(st_w-10)/2}" y="{y+26+k*17}" font-size="13.5" font-weight="700" fill="#fff" text-anchor="middle">{esc(ln)}</text>'
        s += f'<text x="{x+(st_w-10)/2}" y="{base_y+22}" font-size="12.5" font-weight="700" fill="{INK}" text-anchor="middle">{esc(price)}</text>'
        s += f'<text x="{x+(st_w-10)/2}" y="{base_y+40}" font-size="12" fill="{MUT}" text-anchor="middle">{esc(term)}</text>'
    s += footer_note(h, note) + '</svg>'
    return s

